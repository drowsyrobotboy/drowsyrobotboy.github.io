/*
 * The following function hides menu on mobile when you tap outside
 */
document.addEventListener('DOMContentLoaded', function() {
  // Select all menu items that have a submenu
  document.querySelectorAll('li > a').forEach(function(link) {
    link.addEventListener('click', function(e) {
      var submenu = this.parentElement.querySelector('ul.rb-sub-nav');
      if (submenu) {
        e.preventDefault(); // Prevent navigation for parent links
        // Toggle submenu
        if (window.getComputedStyle(submenu).display === 'none') {
          submenu.style.display = 'block';
        } else {
          submenu.style.display = 'none';
        }
      }
    });
  });

  // Hide submenu when clicking elsewhere
  document.addEventListener('click', function(e) {
    if (!e.target.closest('li')) {
      document.querySelectorAll('ul.rb-sub-nav').forEach(function(ul) {
        if (window.getComputedStyle(ul).display !== 'none') {
          ul.style.display = 'none';
        }
      });
    }
  });
});